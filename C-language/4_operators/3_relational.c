

#include <stdio.h>

int main()
{

    int a = 20;

    int b = 10;

    printf("\n Greater than %d\n", a > b);

    printf("\n Less than %d\n", a < b);

    printf("\n Greater or equal  %d\n", a >= b);

    printf("\n Less or equal  %d\n", a <= b);

    printf("\n compare  %d\n", a == b);

    printf("\n not equal  %d\n", a != b);

    return 0;
}