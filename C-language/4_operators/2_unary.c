

#include <stdio.h>

int main()
{

    int a = 5;

    // performing operation on single operands

    // pre increment

    printf("\n a Value %d\n", a);

    printf("\npreIncrement Value %d\n", ++a);

    //  pre decrement

    printf("\npreDecrement Value %d\n", --a);

    //    post increment

    printf("\npost Increment Value %d\n", a++);

    printf("\n now checking value of A Value %d\n", a);

    //    post decrement

    printf("\npost decrement Value %d\n", a--);

    printf("\n now checking value of A Value %d\n", a);

    return 0;
}