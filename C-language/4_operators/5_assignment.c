

#include <stdio.h>

int main()
{

    // here  equal symbol is assignment operator
    int num = 24;

    // add and assign +=

    printf("\nadd and Assign %d\n ", num += 24);

    printf("\nSubstaction and Assign %d\n ", num -= 24);

    printf("\nMultiPlication and Assign %d\n ", num *= 24);

    printf("\nDivision and Assign %d\n ", num /= 24);

    printf("\nModulo and Assign %d\n ", num %= 24);

    return 0;
}